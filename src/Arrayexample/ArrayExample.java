package Arrayexample;

public class ArrayExample {
    public static void main(String[] args) {
        int [] marks = new int [7];
        marks[0] = 90;
        marks [1] = 80;
        marks[2] = 70;
        marks[3] = 60;
        marks[4] = 99;
        marks [5] = 88;
        marks [6] = 77;
        for(int i=0; i<7; i++){
            System.out.println(marks[i]);
        }


    }
}
