package Arrayexample;

public class ArrayExample {
    public static void main(String[] args) {
        int [] marks = new int [9];
        marks[0] = 90000;
        marks [1] = 80000;
        marks[2] = 70000;
        marks[3] = 60000;
        marks[4] = 99000;
        marks [5] = 200000;
        marks [6] = 100000;
        marks [7] = 888880;
        marks [8] = 999990;
        for(int i=0; i<9; i++){
            System.out.println(marks[i]);
        }


    }
}
