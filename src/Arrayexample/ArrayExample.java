package Arrayexample;

public class ArrayExample {
    public static void main(String[] args) {
        int [] marks = new int [9];
        marks[0] = 900;
        marks [1] = 800;
        marks[2] = 700;
        marks[3] = 600;
        marks[4] = 990;
        marks [5] = 200000;
        marks [6] = 100000;
        marks [7] = 888880;
        marks [8] = 999990;
        for(int i=0; i<7; i++){
            System.out.println(marks[i]);
        }


    }
}
