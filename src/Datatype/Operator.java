package Datatype;

public class Operator {
    public static void main(String[] args) {
        //relational operator
        System.out.println(90>076);
        System.out.println(68<45);
        System.out.println(34<=35);
        System.out.println(45>=78);
        System.out.println(45<=45);
        //logical operator
        System.out.println(90>89 && 89>99);
        System.out.println(78<=79 || 89<=89);

    }
}
